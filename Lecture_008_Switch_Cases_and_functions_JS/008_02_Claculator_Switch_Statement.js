
let a = 4
let b = 2
let operator = '/'
switch (operator){
   case '+': 
      console.log(`The sum of the given numbers is: ${a + b}`)
      break 
      case '-': 
           console.log(`The answer is: ${a - b}`)
      break 
      case '*': 
           console.log(`The answer is:  ${a * b}`)
      break 
      case '/': 
           console.log(`The answer is:  ${a / b}`)
      break 
      default:
           console.log('Invalid Operation')
}

// Output:   // for +
// The sum of the given numbers is: 6

// Output:   // for /
// The answer is:  2



